class AccountsScreen extends ConsumerStatefulWidget {
  const AccountsScreen({super.key});

  @override
  ConsumerState<AccountsScreen> createState() => _AccountsScreenState();
}

class _AccountsScreenState extends ConsumerState<AccountsScreen> {
  @override
  Widget build(BuildContext context) {
    final accountsAsync = ref.watch(getAccountsProvider);
    final netWorthAsync = ref.watch(getNetWorthProvider);
    final currencySymbol = ref.watch(currencySymbolProvider);
    final isFirstMonth = ref.watch(isFirstMonthOfActivityProvider);
    return Scaffold(
      backgroundColor: Theme.of(context).scaffoldBackgroundColor,
      appBar: AppBar(
        title: Text('Accounts'),
        forceMaterialTransparency: true,
        actions: [
          DecoratedBox(
            decoration: BoxDecoration(
              color: AppColors.kPrimaryColor,
              shape: BoxShape.circle,
            ),
            child: Padding(
              padding: const EdgeInsets.all(6),
              child: GestureDetector(
                onTap: () => context.push(AppRoutes.addAccount.path),
                child: Icon(
                  Icons.add,
                  color: Colors.black,
                ),
              ),
            ),
          ),
          gapW20,
        ],
      ),
      body: Padding(
        padding: const EdgeInsets.symmetric(
          horizontal: Sizes.kHorizontalPadding,
          vertical: Sizes.kVerticalPadding,
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.stretch,
          children: [
            Text(
              'Total Balance',
              style: TextStyles.subtitle.copyWith(fontSize: 14),
              textAlign: TextAlign.center,
            ),
            Row(
              mainAxisAlignment: MainAxisAlignment.center,
              crossAxisAlignment: CrossAxisAlignment.center,
              children: [
                Text(
                  netWorthAsync.when(
                    data: (data) => '$currencySymbol${data.toStringAsFixed(2)}',
                    error: (error, stackTrace) => 'Error',
                    loading: () => '${currencySymbol}0.00',
                  ),
                  style: TextStyles.title,
                  textAlign: TextAlign.center,
                ),
                gapW4,
                isFirstMonth.value == null || isFirstMonth.value!
                    ? SizedBox.shrink()
                    : LastMonthContainer(
                        savingPercentage: 2,
                        isShrinked: true,
                      ),
              ],
            ),
            gapH16,
            Expanded(
              child: SingleChildScrollView(
                child: accountsAsync.when(
                  data: (accounts) {
                    if (accounts.isEmpty) {
                      return Center(
                        child: Text(
                          'No accounts added yet.',
                          style: TextStyles.subtitle.copyWith(
                            color: Colors.grey,
                          ),
                        ),
                      );
                    }
                    return SettingsSection(
                      backgroundColor: Theme.of(context).cardColor,
                      widgets: List.generate(
                        accounts.length,
                        (index) {
                          return AccountCard(
                            accountType: accounts[index].accountType.name,
                            accountName: accounts[index].accountName,
                            balance: accounts[index].balance,
                            currentBalance: accounts[index].currentBalance,
                            currencySymbol: currencySymbol,
                          );
                        },
                      ),
                    );
                  },
                  loading: () => Center(
                    child: CircularProgressIndicator(),
                  ),
                  error: (error, stackTrace) => Center(
                    child: Text(error.toString()),
                  ),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}

class AccountCard extends StatelessWidget {
  const AccountCard({
    super.key,
    required this.accountType,
    required this.accountName,
    required this.balance,
    required this.currentBalance,
    required this.currencySymbol,
  });
  final String accountType;
  final String accountName;
  final double balance;
  final double currentBalance;
  final String currencySymbol;
  @override
  Widget build(BuildContext context) {
    return CustomCard(
      child: Padding(
        padding: const EdgeInsets.symmetric(horizontal: 8),
        child: Row(
          children: [
            Icon(
              Icons.account_balance,
              color: AppColors.kPrimaryColor,
            ),
            gapW12,
            Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  accountName,
                  style: TextStyles.title.copyWith(fontSize: 16),
                ),
                Text(
                  '$currencySymbol${balance.round()}',
                  style: TextStyles.subtitle.copyWith(fontSize: 14),
                ),
              ],
            ),
            Spacer(),
            Column(
              crossAxisAlignment: CrossAxisAlignment.end,
              children: [
                Text(
                  '$currencySymbol${balance.toStringAsFixed(2)}',
                  style: TextStyles.title.copyWith(fontSize: 16),
                ),
                Text(''),
              ],
            ),
          ],
        ),
      ),
    );
  }
}

extension AccountModelX on AccountModel {
  IconData get accountTypeIcon {
    switch (accountType.index) {
      case 0:
        return Icons.credit_card;
      case 1:
        return Icons.credit_card;
      case 2:
        return Icons.wallet;
      default:
        return FontAwesomeIcons.arrowTrendUp;
    }
  }
}
